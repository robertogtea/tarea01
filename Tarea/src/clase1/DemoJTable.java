        String userText = this.jTextPane1.getText();
        String columnNames[] = this.get_columns_names(userText);
        String dataValues[][] = this.get_columns_values(userText);
        
        TableModel model = this.jTable1.getModel();
        System.out.println("Columns "+model.getColumnCount());
        for (int x=0; x<dataValues.length; x++){
            for(int y=0; y<dataValues[x].length; y++){
             model.setValueAt(dataValues[x][y], x, y);
            }
        }
        
        //this.jTable1.setModel(model);
        this.jTable1.createDefaultColumnsFromModel();
        
        TableColumnModel cmodel = this.jTable1.getColumnModel();
        
        TableColumn column;
        for(int x=0; x<columnNames.length; x++){
            column = cmodel.getColumn(x);
            column.setHeaderValue(columnNames[x]);
        }        
