    public static String[] mySplit(String text, String delimiter) {
        java.util.List<String> parts = new java.util.ArrayList<String>();

        text += delimiter;

        for (int i = text.indexOf(delimiter), j=0; i != -1;) {
            String temp = text.substring(j,i);
            if(temp.trim().length() != 0) {
                parts.add(temp);
            }
            j = i + delimiter.length();
            i = text.indexOf(delimiter,j);
        }

        return parts.toArray(new String[0]);
    }
    
    
    private String[] get_columns_names(String texto){
        String[] lineas = texto.split("\n");
        lineas=mySplit(lineas[0], "|");
        
        return lineas;
    }
    
   private String[][] get_columns_values(String texto){
        String[] lineas = texto.split("\n");
        String[][] valores = new String[lineas.length -1][];
        for(int x=1; x<lineas.length; x++){
            valores[x-1] = mySplit(lineas[x], "|");
        }
        return valores;
    }
   

    
